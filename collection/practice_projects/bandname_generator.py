# create a greeting
print("Welcome to the bandname generator created in the 100 Days of code course")

# ask the user for the city that they grew up in
city = input("What's the name of the city you grew up in?\n")

# ask the user for the name of a pet
pet = input("What's your pet name?\n")

# combine the name and their city and pet and show them their band name
bandname = city + " " + pet
print("Your band name could be " + bandname)

